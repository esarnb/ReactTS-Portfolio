export function getSprite(stage: Stage, mood: Mood): Sprite {
  const key = `${stage}-${mood}`;
  if (key in SPRITES) {
    return SPRITES[key];
  }
  const fallback = `${stage}-idle`;
  if (fallback in SPRITES) {
    return SPRITES[fallback];
  }
  return EGG_IDLE;
}
